export function formatCurrency(amount: number, currencyCode: CurrencyCode): string {
  const currency = CURRENCIES.find(c => c.code === currencyCode)
  if (!currency) return `$${amount}`
  
  const convertedAmount = amount * EXCHANGE_RATES[currencyCode]
  
  if (currencyCode === 'JPY' || currencyCode === 'KRW') {
    return `${currency.symbol}${Math.round(convertedAmount).toLocaleString()}`
  }
  
  return `${currency.symbol}${convertedAmount.toFixed(2)}`
}

export function t(key: string, lang: LanguageCode, replacements?: Record<string, string | number>): string {
  const translation = translations[lang]?.[key] || translations.en[key] || key
  
  if (!replacements) return translation
  
  return Object.entries(replacements).reduce((text, [placeholder, value]) => {
    return text.replace(`{${placeholder}}`, String(value))
  }, translation)
}
